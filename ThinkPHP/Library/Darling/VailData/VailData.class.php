class VailData {


	/*
	 * is_phone();
	 * 
	 * 判断是否为手机
	 */
	public function is_phone($phone){
		
		if(preg_match("/^1\d{10}$/", $phone)){
			return true;
		}	
		
		return false;
	}
	
}
